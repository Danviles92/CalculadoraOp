package calculadora;

/**
 *
 * @author Danviles
 */
public class CalBasico {

    public CalBasico() {
        
    }
    
    public int setSuma(int a, int b){
        return a+b;
    }
    public int setResta(int a, int b){
        return a-b;
    }
    public int setMul(int a, int b){
        return a*b;
    }
    public int setDiv(int a, int b){
        return a/b;
    }
    public int setMod(int a, int b){
        return a%b;
    }
    
    
}
